.global _start
_start:
		MOV R4, #0
		MOV R5, #HEX_CODES
		LDR R12, =0xFF200020
		LDR R11, =0xFF200050
MLOOP:	LDR R10, [R11]		
		CMP R10, #0
		BNE DELAY
		B MLOOP

DELAY:  MOV R9, R10
DLOOP:	LDR R10, [R11]
		CMP R10, #0
		BNE DLOOP
		CMP R9, #0b0001
		BEQ ZERO
		CMP R9, #0b0010
		BEQ INC
		CMP R9, #0b0100
		BEQ DEC
		B MLOOP
		
ZERO:	MOV R4, #0
		B DISPLAY
	
INC:	ADD R4, #1
		B DISPLAY

DEC:	SUB R4, #1
		B DISPLAY
	
DISPLAY:
		CMP R4, #9
		MOVGT R4, #0
		CMP R4, #0
		MOVLT R4, #0
		MOV R6, R5
		ADD R6, R4
		LDRB R0, [R6]
		STR R0, [R12]
		B MLOOP
		
HEX_CODES: .byte 0b00111111, 0b00000110, 0b01011011, 0b01001111, 0b01100110
           .byte 0b01101101, 0b01111101, 0b00000111, 0b01111111, 0b01100111
		   .skip 2